let numero = 1;  // inicia-se com o número 1

do {
    console.log("Numero atual:", numero);  // mostra o número atual
    numero++;  // incrementa o número em 1 a cada iteração
} while (numero <= 5);  // continua enquanto o número for menor ou igual a 5

console.log("Numero maior que 5 gerado:", numero);  //mostra o número final
