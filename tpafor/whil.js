// Variáveis 
let soma = 0;  // inicia-se a soma igual a 0
let i = 1;     // inicia-se com o número 1

//enquanto a soma for menor ou igual a 20
while (soma <= 20) {
    soma += i;  // Soma o valor de i à variável soma
    i++;        // Incrementa o valor de i para somar o próximo número
}

console.log("A soma dos números até que o total seja maior que 20 é:", soma);

